const month = "march";
switch (month) {
    case "jan":
        console.log(`january`);
        break;
    case "feb":
        console.log(`fabruary`);
        break;
    case "march":
        console.log(`March`);
        break;
    case "April":
        console.log(`April`);
        break;
    case "May":
        console.log(`May`);
        break;
    case "June":
        console.log(`june`);
        break;

    default:
        console.log("default case matched")
        break;
}

